package week1.day5;

public class Print100WithoutNumbers {

	public static void main(String[] args) {

		//Z-Z=0, d=100
		
		for (int i = 'Z'-'Z'; i <= 'd'; i++) { System.out.println(i); }
		 
		  
		int i = 'd'-'A';
		System.out.println(i); //35
		
		char j = 'd'-'A';
		System.out.println(j); //#
	}

}
